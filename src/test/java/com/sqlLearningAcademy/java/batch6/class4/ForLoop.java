package com.sqlLearningAcademy.java.batch6.class4;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i <= 15; ++i) {
            System.out.println("class number " + i);
        }

        for (int j = 10; j > 5; --j) {
            System.out.println("time out " + j);
        }
    }
}
