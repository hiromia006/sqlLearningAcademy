package com.sqlLearningAcademy.java.batch6.class2;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int subject1mark=80, subject2Mark=10;

        System.out.println(subject1mark+subject2Mark);//90
        System.out.println(subject1mark-subject2Mark);//70
        System.out.println(subject1mark/subject2Mark);//8

        System.out.println(subject1mark%subject2Mark);//0

    }
}
