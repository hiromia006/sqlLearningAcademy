package com.sqlLearningAcademy.java.batch6.class2;

public class UnaryOperator {
    public static void main(String[] args) {
        int number = 50;
        System.out.println(number++);
        System.out.println(++number);
        System.out.println(number++);
        System.out.println(--number);
        System.out.println(number--);

    }
}
