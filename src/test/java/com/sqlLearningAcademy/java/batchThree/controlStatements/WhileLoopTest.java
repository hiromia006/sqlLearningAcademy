package com.sqlLearningAcademy.java.batchThree.controlStatements;

public class WhileLoopTest {
    public static void main(String[] args) {
        //increment
        for (int i = 0; i < 10; i++) {
            System.out.println("Welcome SQA Learning Academy " + i);
        }

        int i = 0;
        while (i < 10) {
            System.out.println("Welcome SQA Learning Academy " + i);
            i++;
        }


    }
}
